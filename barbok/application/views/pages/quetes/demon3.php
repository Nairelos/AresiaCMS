	<br />
	<strong>L'Ordre de l'Oeil Putride, disciple de Brumaire</strong><br />
    Ce sont les Espions. Vous pouvez joindre cet Ordre en allant parler à Ledrob Terceséc au sous-sol de la Tour de Brâkmar. Pour être accepté, il faut lui ramener un oeil de corbac.<br />
    Cet Ordre permet d'utiliser des potions d'Objouflage (transformation de votre perso en divers objets de décor, assez sympa pour le côté RP ou même le fun)<br />
    <br />
    <strong>L'Ordre du Coeur Saignant, disciple de Djaul</strong><br />
    Ce sont les Guerriers. Vous pouvez joindre cet Ordre en allant parler à Kaffra Kyper au 1er étage de la Tour de Brâkmar. Pour être accepté, il faut lui ramener un os de chafer.<br />
    Cet ordre permet de faire perdre plus d’énergie aux ennemis vaincus (+ 200 points) et d'utiliser des potions d’agression de niveau 1 soit 1 case d’agression automatique (ordre plutôt reservé à ceux qui font du PvP régulièrement et souhaitent affaiblir leurs adversaires)<br />
<br />
<strong>L'ordre de l'Esprit Malsain, disciple d'Hecate</strong><br />
Ce sont les Prêtres. Vous pouvez joindre cet Ordre en allant parler à Divad Dleifrepok au 2ème étage de la Tour de Brâkmar. Pour être accepté, il faut lui ramner un poil de barbe de Bwork Mage.<br />
Cet ordre permet d'utiliser des potions de résurrection (l'odre des mules par excellence pour ressuciter un de vos autres perso, mais au sein d'une guilde cela peut etre la personne qui va allez aider les autres)<br />
