	<p class="adonjon">Dis Immo, c'est quoi un Obvijevans ?</p>
	<br />
	C'est un objet qui vient se rajouter sur votre équipement, pour changer son apparence. Ils n'apportent aucun bonus, ils permettent juste de changer l'aspect de votre equipement.<br />
	<br />
	Il offre de ce fait un avantage stratégique (enfin pour la cape et la coiffe) puisque l'ennemi ne sait alors plus l'equipement que vous avez. L'adversaire ne sait pas à l'avance si vous jouez terre, air, feu ou air par exemple puisque votre equipement à l'apparence de votre obvijevan.<br />
	<br />
	Enfin, il flood pas mal, et disent pas mal de conneries, ce qui en fait un compagnon de jeu.<br />
	<br />
	<br />
	<p class="adonjon">Dis immo, comment je l'obtiens ?</p>
	<br />
	Il s'agit de cadeau d'abonnement :<br />
	- Anneau : 1 mois d'abonnement (soit 5 euros)<br />
	- Amulettes : 3 mois d'abonnement (soit 14,50 euros)<br />
	- Capes : 6 mois d'abonnement (soit 27,5 euros)<br />
	- Coiffe : 1 an d'abonnement (soit 48 euros)<br />
	<br />
	Aprés 2 mois les propriétaires peuvent aussi les vendres, vous pourrez donc en trouvez à acheter<br />
	<br />
	<br />
	<p class="adonjon">Dis immo, comment on le fait évoluer ?</p>
	<br />
	Les objets vivants peuvent être nourris toutes les douze heures au minimum. Contrairement aux familiers, il n'est pas nécessaire de les
nourrir régulièrement, si un Obvijevan n'est pas nourri pendant longtemps, il se contentera de vous réclamer à manger !<br />
<br />

Chaque type d'objet vivant dispose de son propre régime alimentaire. Les capes Obvijevans mangent des capes, les chapeaux Obvijevans mangent
des chapeaux, les amulettes Obviejvans mangent des amulettes et les anneaux Obvijevans mangent des anneaux. Plus l'objet que vous leur donnerez est puissant (plus son niveau est important), plus l'Obvijevan évoluera rapidement. Attention l'objet que vous lui donner à manger est perdu !<br />
<br />
Lorsqu'un Obvijevan a eu suffisamment à manger, il se métamorphose et peut prendre une autre apparence. Chaque Obvijevan dispose de vingt métamorphoses différentes.
