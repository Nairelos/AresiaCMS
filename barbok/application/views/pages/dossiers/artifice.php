	<p class="donjon">Les Fées d'Artifices</p>


<br />
<br />
<br />
Tout d'abord il faut savoir que pour faire des fées d'artifices il faut absolument être aligné (ange ou démon), vous pourrez fabriquer des fées quand le village des brigandins sera de votre alignement (c'est un village qu'on peut conquérir si le coeur vous en dit). Les fées d'artifices ne servent à rien dans le jeu mis à part célebrer des évenements par exemple (genre pour les mariages et autres).<br />
<br />
Avant de partir pour le village il faut acheter des poudres d'artifices, elles coutent 100 kamas, vous les trouverez à Brakmar auprés du PNJ en -29/34 et à Bonta auprés du PNJ en -27/-58<br />
<br />
<img class="barb" src="<?php echo DIRR; ?>assets/images/dossiers/artifice/arti1.jpg" alt="" /><br />
<br />
Si vous n'avez pas le zaap du village brigandin, allez au zaap en -20/-20, l'entrée du village est en -19/-23.<br />
<br />
<img class="barb" src="<?php echo DIRR; ?>assets/images/dossiers/artifice/arti2.jpg" alt="" /><br />
<img class="barb" src="<?php echo DIRR; ?>assets/images/dossiers/artifice/arti3.jpg" alt="" /><br />
<br />
Si le village est déjà de votre alignement parlez à la dame pour monter, sinon il faudra allez en haut, affronter le groupe de garde, attendre que la porte s'ouvre puis battre le prisme pour pouvoir entrer.<br />
