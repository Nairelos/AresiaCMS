<strong>Comment elever une dinde ?</strong><br />
<br />
<br />
<span class="beau">Les dindes sauvages :</span><br />
<br />
Avant tout, il vous faut le sort "apprivoisement de monture" qui s'obtient à la fin du <a class="panop" href="<?php echo DIR; ?>donjons/koulossed">Donjon Koulosse</a> en amenant 50 feuilles et 50 fleurs de kalyptus. Plus vous montez le sort plus vous avez de tour pour tenter de tuer la dinde.<br />
<br />
Les dindes en combat desenvoutes les personnages. Le sort etant un état, le desenvoutement n'a donc pas d'effet sur la capture. A la fin du sort, le message "joueur est sorti de l'état apprivoisement" apparaît en vert dans le chat.<br />
<br />
Il vous faudra également vous munir d'un filet qui s'achéte pour 10k au PNJ en -17/0, il faut le mettre à la place de votre arme. Le filet est perdu si la dinde est capturée.<br />
<br />
<br />
Types de dindes :<br />
<br />
Plus le niveau d'une dinde monte plus elle vous apporte des bonus, parfois bien supérieur à ceux d'un familier. Une monture ne perd pas de point de vie et n'a pas besoin d'etre nourrie. Elle peux monter au level 100 maximum.<br />
<br />
<br />
On peut echanger les dindes contre des parchemins en 5/-6 au Garde montay.<br /><br />
